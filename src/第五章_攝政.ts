import { 大逆罪例外 } from "./第一章_皇位継承";

// ━━━ 型定義 ━━━

/** 攝政事由 */
type 攝政事由 =
  | "未成年"     // 天皇ガ成年ニ達セザル場合
  | "久曠大政"   // 天皇久シク大政ヲ親ラスルコト能ハザル場合
  | "勅旨";      // 天皇自ラ攝政ヲ置クコトヲ命ジタル場合

/** 攝政候補ノ順位 */
interface 攝政候補 {
  readonly 御名: string;
  readonly 順位: number;
  readonly 資格: string;
}

/** 攝政プロセス */
interface 攝政プロセス {
  readonly 御名: string;
  readonly 事由: 攝政事由;
  readonly 就任日: Date;
  readonly 委任權限: ReadonlyArray<string>;
  活動中: boolean;
}

// ━━━ 攝政就任順位（Proxy Delegation Order） ━━━

/**
 * 第二十三條
 * 天皇未タ成年ニ達セサルトキハ攝政ヲ置ク
 *
 * 第二十四條
 * 天皇久シク大政ヲ親ラスルコト能ハサルトキハ
 * 勅旨ヲ以テ攝政ヲ置ク
 *
 * 第二十五條
 * 攝政ハ左ノ順序ニ依リ成年ニ達シタル皇族之ニ任ス
 * 一 皇太子又ハ皇太孫
 * 二 親王及王
 * 三 皇后
 * 四 皇太后
 * 五 太皇太后
 * 六 內親王及女王
 */
const 攝政就任順位: ReadonlyArray<string> = Object.freeze([
  "皇太子又ハ皇太孫",
  "親王及王（長系ヲ先ニシ同等ナルトキハ長ヲ先ニス）",
  "皇后",
  "皇太后",
  "太皇太后",
  "內親王及女王（長系ヲ先ニシ同等ナルトキハ長ヲ先ニス）",
]);

// ━━━ 攝政就任（Proxy Activation） ━━━

/**
 * 天皇プロセスガ正常稼働不能ノ際、
 * 攝政プロセス（Proxy）ヲ起動シ、
 * 天皇ノ名ニ於テ大權ヲ代行セシム。
 */
export function 攝政就任(候補: 攝政候補, 事由: 攝政事由): 攝政プロセス {
  console.log("━━━ 攝政就任手續 ━━━");
  console.log(`【攝政就任】事由: ${事由}`);
  console.log(`【攝政】${候補.御名}、攝政ニ就任ス。（順位: ${候補.順位}）`);

  /**
   * 第二十六條
   * 攝政ハ天皇ノ名ニ於テ大權ヲ行フ
   *
   * 攝政プロセスニ委任セラルル權限一覽。
   * Root權限ソノモノハ天皇ニ留保セラル。
   */
  const 委任權限: ReadonlyArray<string> = Object.freeze([
    "法律ノ裁可及公布",
    "帝國議會ノ召集・開會・閉會・停會",
    "衆議院ノ解散",
    "官吏ノ任免",
    "外國使臣ノ接受",
    "大赦・特赦・減刑・復權ノ命令",
  ]);

  console.log("【委任權限】以下ノ大權ヲ天皇ノ名ニ於テ行使ス:");
  for (const 權限 of 委任權限) {
    console.log(`  ✦ ${權限}`);
  }

  return Object.freeze({
    御名: 候補.御名,
    事由,
    就任日: new Date(),
    委任權限,
    活動中: true,
  });
}

// ━━━ 攝政解任（Proxy Deactivation） ━━━

/**
 * 第二十七條
 * 攝政ヲ置クノ原因止ミタルトキハ
 * 其ノ攝政ハ之ヲ廢ス
 *
 * 天皇プロセスガ正常稼働ニ復歸シタル場合、
 * 攝政Proxyプロセスヲ停止シ、直接統治ニ復ス。
 */
export function 攝政解任(攝政: 攝政プロセス): void {
  console.log("━━━ 攝政解任 ━━━");
  console.log(`【攝政解任】${攝政.御名}、攝政ノ任ヲ解カル。`);
  console.log("【復歸】天皇陛下、親ラ大政ヲ總攬シ給フ。");

  // NOTE: Object.freeze 濟ノ爲直接變更不可。
  // 實際ニハ新タナル状態オブジェクトヲ生成スベシ。
}

// ━━━ 攝政ノ制限 ━━━

/**
 * 第二十八條
 * 攝政ハ皇室典範及皇室ノ制度ヲ變更スルコトヲ得ス
 *
 * 攝政Proxyプロセスニハ、皇室典範（本システム）ノ
 * 設定變更權限ハ一切委任セラレズ。
 * Write操作ヲ試ミタル場合ハ大逆罪例外ヲスロース。
 */
export function 攝政權限檢査(操作: string): void {
  const 禁止操作 = [
    "皇室典範ノ改正",
    "皇室制度ノ變更",
    "皇位繼承順位ノ改定",
    "皇族會議構成ノ變更",
  ];

  if (禁止操作.some((禁止) => 操作.includes(禁止))) {
    throw new 大逆罪例外(
      "攝政",
      `攝政ハ「${操作}」ヲ行フ權限ヲ有セズ。` +
      `皇室典範第二十八條ニ依リ之ヲ禁ズ。`
    );
  }
}
