import { 大逆罪例外 } from "./第一章_皇位継承";

// ━━━ 型定義 ━━━

/** 御料ノ種別 */
type 御料種別 = "土地" | "森林" | "建物" | "寶物" | "神器" | "其ノ他";

/** 世傳御料 */
interface 世傳御料 {
  readonly 名稱: string;
  readonly 種別: 御料種別;
  readonly 所在地: string;
  readonly 登錄日: Date;
  readonly 讓渡不可: true;   // 常ニ true — 讓渡ハ永久ニ不可
  readonly 分讓不可: true;   // 常ニ true — 分讓ハ永久ニ不可
}

// ━━━ 御料臺帳（Immutable Asset Registry） ━━━

const 御料臺帳: 世傳御料[] = [];

// ━━━ 御料登錄 ━━━

/**
 * 第四十七條
 * 皇室ノ世傳御料ハ讓渡スルコトヲ得ス
 * 又分讓スルコトヲ得ス
 *
 * 世傳御料ハ一度登錄セラレタル後、
 * 永久ニ讓渡・分讓・削除不可ナリ。
 * Append-Only ノ不變臺帳（Immutable Ledger）ニ登錄セラル。
 */
export function 御料登錄(
  名稱: string,
  種別: 御料種別,
  所在地: string,
): 世傳御料 {
  const 新御料: 世傳御料 = Object.freeze({
    名稱,
    種別,
    所在地,
    登錄日: new Date(),
    讓渡不可: true as const,
    分讓不可: true as const,
  });

  御料臺帳.push(新御料);

  console.log(
    `【御料登錄】「${名稱}」（${種別}）ヲ世傳御料臺帳ニ登錄ス。` +
    `所在地: ${所在地}`
  );

  return 新御料;
}

// ━━━ 讓渡・分讓ノ禁止 ━━━

/**
 * 第四十七條（續キ）
 * 但シ一時ノ處分ハ此ノ限ニ在ラス
 *
 * 讓渡・分讓ヲ試ミタル場合ハ大逆罪例外ヲスロース。
 * 唯一ノ例外ハ「一時ノ處分」（Temporary Lease）ノミ。
 */
export function 讓渡禁止檢査(御料名稱: string, 操作: "讓渡" | "分讓"): never {
  throw new 大逆罪例外(
    "不明",
    `世傳御料「${御料名稱}」ノ${操作}ヲ試ミル。` +
    `皇室典範第四十七條ニ依リ永久ニ之ヲ禁ズ。`
  );
}

/**
 * 一時ノ處分（Temporary Lease）。
 * 御料ノ所有權ヲ移轉セズ、一時的ナ使用許可ノミ。
 */
export function 一時處分(
  御料名稱: string,
  處分內容: string,
  期限日: Date,
): void {
  const 對象 = 御料臺帳.find((料) => 料.名稱 === 御料名稱);

  if (!對象) {
    throw new Error(`【錯誤】「${御料名稱}」ハ御料臺帳ニ存在セズ。`);
  }

  console.log(
    `【一時處分】「${御料名稱}」ニ對シ一時ノ處分ヲ行フ。` +
    `內容: ${處分內容}、期限: ${期限日.toISOString()}`
  );
}

// ━━━ 御料ノ抵當禁止 ━━━

/**
 * 第四十八條
 * 世傳御料ハ抵當ト爲スコトヲ得ス
 *
 * 御料ヲ擔保ト爲スコトハ一切之ヲ禁ズ。
 */
export function 抵當禁止檢査(御料名稱: string): never {
  throw new 大逆罪例外(
    "不明",
    `世傳御料「${御料名稱}」ヲ抵當ト爲サントス。` +
    `皇室典範第四十八條ニ依リ一切之ヲ禁ズ。`
  );
}

// ━━━ 御料追加指定 ━━━

/**
 * 第四十九條
 * 世傳御料ノ品目ハ別ニ之ヲ定ム
 *
 * 御料ノ品目ハ勅令ヲ以テ別途定メラル。
 */
export function 御料品目一覽(): ReadonlyArray<世傳御料> {
  return Object.freeze([...御料臺帳]);
}

