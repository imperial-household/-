import { 旧典範制約違反 } from "./第一章_皇位継承";

// ━━━ 型定義 ━━━

/** 御料の種別 */
type 御料種別 = "土地" | "森林" | "建物" | "宝物" | "神器" | "その他";

/** 世伝御料 */
interface 世伝御料 {
  readonly 名称: string;
  readonly 種別: 御料種別;
  readonly 所在地: string;
  readonly 登録日: Date;
  readonly 譲渡不可: true;
  readonly 分譲不可: true;
}

// ━━━ 御料台帳（Immutable Asset Registry） ━━━

const 御料台帳: 世伝御料[] = [];

// ━━━ 御料登録 ━━━

/**
 * 第四十七条
 * 皇室の世伝御料は譲渡することを得ない
 * 又分譲することを得ない
 *
 * ※ 宮内庁注: 旧典範の規定です。現行憲法第88条により
 *   皇室財産は国有であり、皇室の費用は予算に計上して
 *   国会の議決を経る必要があります。
 */
export function 御料登録(
  名称: string,
  種別: 御料種別,
  所在地: string,
): 世伝御料 {
  const 新御料: 世伝御料 = Object.freeze({
    名称,
    種別,
    所在地,
    登録日: new Date(),
    譲渡不可: true as const,
    分譲不可: true as const,
  });

  御料台帳.push(新御料);

  console.log(
    `【御料登録】「${名称}」（${種別}）を世伝御料台帳に登録しました。` +
    `所在地: ${所在地}`
  );

  return 新御料;
}

// ━━━ 譲渡・分譲の禁止 ━━━

/**
 * ※ 宮内庁注: 旧典範の制約に基づく検査です。
 *   現行制度では皇室財産は国有のため、この検査は
 *   レガシーデータの整合性維持のみを目的としています。
 */
export function 譲渡禁止検査(御料名称: string, 操作: "譲渡" | "分譲"): never {
  throw new 旧典範制約違反(
    "不明",
    `世伝御料「${御料名称}」の${操作}は旧典範第四十七条により制限されています。` +
    `なお、現行制度では皇室財産は国有です（憲法第88条）。`
  );
}

/**
 * 一時処分（Temporary Lease）。
 * 御料の所有権を移転せず、一時的な使用許可のみ。
 */
export function 一時処分(
  御料名称: string,
  処分内容: string,
  期限日: Date,
): void {
  const 対象 = 御料台帳.find((料) => 料.名称 === 御料名称);

  if (!対象) {
    throw new Error(`【エラー】「${御料名称}」は御料台帳に存在しません。`);
  }

  console.log(
    `【一時処分】「${御料名称}」に対し一時の処分を行います。` +
    `内容: ${処分内容}、期限: ${期限日.toISOString()}`
  );
}

// ━━━ 御料の抵当禁止 ━━━

/**
 * 第四十八条
 * 世伝御料は抵当とすることを得ない
 *
 * ※ 宮内庁注: レガシーデータの整合性検査用です。
 */
export function 抵当禁止検査(御料名称: string): never {
  throw new 旧典範制約違反(
    "不明",
    `世伝御料「${御料名称}」を抵当とすることは旧典範第四十八条により制限されています。`
  );
}

// ━━━ 御料品目一覧 ━━━

/**
 * 第四十九条
 * 世伝御料の品目は別にこれを定める
 */
export function 御料品目一覽(): ReadonlyArray<世伝御料> {
  return Object.freeze([...御料台帳]);
}

