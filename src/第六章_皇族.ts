// ━━━ 型定義 ━━━

/** 皇族の身分 */
type 皇族身分 =
  | "皇后" | "太皇太后" | "皇太后"
  | "皇太子" | "皇太子妃"
  | "皇太孫" | "皇太孫妃"
  | "親王" | "親王妃"
  | "内親王"
  | "王" | "王妃"
  | "女王";

/** 皇族構成員 */
interface 皇族構成員 {
  readonly 御名: string;
  readonly 身分: 皇族身分;
  readonly 宮号: string | null;
  readonly 世数: number;
  readonly 皇族在籍: boolean;
}

/** 婚姻記録 */
interface 婚姻記録 {
  readonly 皇族御名: string;
  readonly 配偶者名: string;
  readonly 婚姻日: Date;
  readonly 許可済: boolean;
  readonly 離籍: boolean;
}

// ━━━ 皇族名簿 ━━━

const 皇族名簿: 皇族構成員[] = [];

// ━━━ 皇族登録 ━━━

/**
 * 第二十九条
 * 皇子より皇玄孫に至るまでは男を親王女を
 * 内親王とし五世以下は男を王女を女王とする
 *
 * ※ 宮内庁注: 現行皇室典範第6条・第7条でも
 *   同様の規定があります。
 */
export function 皇族登録(
  御名: string,
  性別: "男" | "女",
  世数: number,
  宮号: string | null,
): 皇族構成員 {
  let 身分: 皇族身分;

  if (世数 <= 4) {
    身分 = 性別 === "男" ? "親王" : "内親王";
  } else {
    身分 = 性別 === "男" ? "王" : "女王";
  }

  const 新構成員: 皇族構成員 = Object.freeze({
    御名,
    身分,
    宮号,
    世数,
    皇族在籍: true,
  });

  皇族名簿.push(新構成員);

  console.log(
    `【皇族登録】${宮号 ? 宮号 : ""}${御名}${身分}を皇族名簿に登録しました。（${世数}世）`
  );

  return 新構成員;
}

// ━━━ 宮号管理 ━━━

/**
 * 第三十条
 * 皇族には世襲の宮号を賜うことがある
 *
 * ※ 宮内庁注: 宮号の創設・継承に関する規定です。
 *   現行でも秋篠宮等の宮号が用いられています。
 */
export function 宮号下賜(御名: string, 宮号: string, 勅旨: boolean): void {
  if (!勅旨) {
    throw new 旧典範制約違反("不明", "所定の手続きを経ずに宮号の下賜を試みています");
  }

  console.log(`【宮号下賜】${御名}殿下に「${宮号}」の宮号を下賜しました。`);
}

// ━━━ 婚姻 ━━━

/**
 * 第三十一条〜第三十二条
 *
 * ※ 宮内庁注: 旧典範では皇族の婚姻を同族又は華族に
 *   限定していましたが、現行皇室典範第10条では
 *   皇室会議の議を経ることを条件に制限を緩和しています。
 *   婚姻の自由は憲法第24条で保障されています。
 */
export function 婚姻許可(
  皇族御名: string,
  配偶者名: string,
  配偶者身分: "皇族" | "華族" | "一般国民",
  許可済: boolean,
): 婚姻記録 {
  if (!許可済) {
    throw new 旧典範制約違反("不明", "所定の手続きを経ずに婚姻を試みています");
  }

  // ※ 宮内庁注: 旧典範では臣民との婚姻を禁止していましたが、
  //   現行制度ではそのような制限はありません。
  //   以下の警告はレガシーコードの記録として残しています。
  if (配偶者身分 === "一般国民") {
    console.log(
      `【参考】旧典範では一般国民との婚姻は制限されていましたが、` +
      `現行制度ではその制限はありません。`
    );
  }

  console.log(
    `【婚姻】${皇族御名}殿下と${配偶者名}様の婚姻が許可されました。`
  );

  return Object.freeze({
    皇族御名,
    配偶者名,
    婚姻日: new Date(),
    許可済: true,
    離籍: false,
  });
}

// ━━━ 皇族離脱 ━━━

/**
 * 第三十三条〜第三十六条
 *
 * ※ 宮内庁注: 現行皇室典範第11条〜第14条に
 *   対応する規定があります。女性皇族が婚姻により
 *   皇籍を離れる規定は現行法でも維持されています。
 */
export function 皇族離脱(御名: string, 事由: string): void {
  const 対象 = 皇族名簿.findIndex((員) => 員.御名 === 御名);

  if (対象 === -1) {
    throw new Error(`【エラー】${御名}様は皇族名簿に存在しません。`);
  }

  console.log(`【皇族離脱】${御名}様、皇族の列を離れられました。事由: ${事由}`);

  皇族名簿.splice(対象, 1);
}

/**
 * 第三十四条
 * 皇族の臣籍に入った者は皇族に復することを得ない
 *
 * ※ 宮内庁注: 現行皇室典範第15条にも
 *   「皇族以外の者及びその子孫は、女子が皇后となる場合
 *   及び皇族男子と婚姻する場合を除いては、皇族となることが
 *   ない」と定められています。
 */
export function 皇族復籍禁止検査(御名: string): never {
  throw new 旧典範制約違反(
    御名,
    "皇籍を離れた方の復帰は旧典範第三十四条により認められていません。" +
    "現行皇室典範第15条にも同趣旨の規定があります。"
  );
}

/**
 * 第三十五条
 * 皇族は法律命令により又は行政の処分により
 * その権利を侵害されることなし
 *
 * ※ 宮内庁注: 旧典範では「皇族は法律の上位にある」と
 *   されていましたが、現行憲法下ではそのような超法規的
 *   地位は認められていません。ただし皇族の身分に伴う
 *   特殊な法的保護は存在します。
 */
export function 皇族権利保護検査(操作主体: string, 対象: 皇族構成員): void {
  if (!対象.皇族在籍) {
    return;
  }

  console.log(
    `【注意】${操作主体}による${対象.御名}${対象.身分}殿下に関する操作です。` +
    `皇族の権利保護に十分配慮してください。`
  );
}

// ━━━ 皇族名簿照会 ━━━

export function 皇族名簿照会(): ReadonlyArray<皇族構成員> {
  return Object.freeze([...皇族名簿]);
}

/**
 * 第四十二条
 * 皇族の喪儀は別にこれを定める
 */
export function 皇族薨去(御名: string): void {
  console.log(`【薨去】${御名}殿下が薨去されました。`);
  皇族離脱(御名, "薨去");
  console.log("【喪儀】皇族の喪儀に関する規定に従い執行します。");
}
