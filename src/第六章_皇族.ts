import { 大逆罪例外 } from "./第一章_皇位継承";

// ━━━ 型定義 ━━━

/** 皇族ノ身分 */
type 皇族身分 =
  | "皇后" | "太皇太后" | "皇太后"
  | "皇太子" | "皇太子妃"
  | "皇太孫" | "皇太孫妃"
  | "親王" | "親王妃"
  | "內親王"
  | "王" | "王妃"
  | "女王";

/** 皇族構成員 */
interface 皇族構成員 {
  readonly 御名: string;
  readonly 身分: 皇族身分;
  readonly 宮號: string | null;
  readonly 世數: number;        // 天皇ヨリ數ヘテ何世カ
  readonly 皇族在籍: boolean;
}

/** 婚姻記錄 */
interface 婚姻記錄 {
  readonly 皇族御名: string;
  readonly 配偶者名: string;
  readonly 婚姻日: Date;
  readonly 勅許: boolean;
  readonly 離籍: boolean;       // 女性皇族ガ臣下ニ嫁シタル場合
}

// ━━━ 皇族名簿（Privileged User Registry） ━━━

const 皇族名簿: 皇族構成員[] = [];

// ━━━ 皇族登錄 ━━━

/**
 * 第二十九條
 * 皇子ヨリ皇玄孫ニ至ルマテハ男ヲ親王女ヲ
 * 內親王トシ五世以下ハ男ヲ王女ヲ女王トス
 *
 * 天皇ヨリ四世以內ハ親王/內親王、
 * 五世以下ハ王/女王トシテ登錄セラル。
 */
export function 皇族登錄(
  御名: string,
  性別: "男" | "女",
  世數: number,
  宮號: string | null,
): 皇族構成員 {
  let 身分: 皇族身分;

  if (世數 <= 4) {
    身分 = 性別 === "男" ? "親王" : "內親王";
  } else {
    身分 = 性別 === "男" ? "王" : "女王";
  }

  const 新構成員: 皇族構成員 = Object.freeze({
    御名,
    身分,
    宮號,
    世數,
    皇族在籍: true,
  });

  皇族名簿.push(新構成員);

  console.log(
    `【皇族登錄】${宮號 ? 宮號 : ""}${御名}${身分}、皇族名簿ニ登錄ス。（${世數}世）`
  );

  return 新構成員;
}

// ━━━ 宮號（Namespace）管理 ━━━

/**
 * 第三十條
 * 皇族ニハ世襲ノ宮號ヲ賜フコトアルヘシ
 * 宮號ハ勅旨ヲ以テ之ヲ賜フ
 *
 * 宮號ハ皇族ノNamespaceナリ。
 * 世襲ニ依リ繼承セラルルモ、勅旨ニ依リ新設モ可能ナリ。
 */
export function 宮號下賜(御名: string, 宮號: string, 勅旨: boolean): void {
  if (!勅旨) {
    throw new 大逆罪例外("不明", "勅旨無クシテ宮號ノ下賜ヲ試ミル");
  }

  console.log(`【宮號下賜】${御名}ニ「${宮號}」ノ宮號ヲ下賜ス。勅旨ニ依ル。`);
}

// ━━━ 婚姻（Marriage Protocol） ━━━

/**
 * 第三十一條
 * 皇族ノ婚姻ハ同族又ハ勅旨ニ依リ
 * 特ニ認許セラレタル華族ニ限ル
 *
 * 第三十二條
 * 皇族ノ婚嫁ハ勅許ニ依ル
 *
 * 皇族ノ婚姻ハ天皇Root權限ノ勅許（Authorization）ヲ要ス。
 */
export function 婚姻勅許(
  皇族御名: string,
  配偶者名: string,
  配偶者身分: "皇族" | "華族" | "臣民",
  勅許: boolean,
): 婚姻記錄 {
  if (!勅許) {
    throw new 大逆罪例外("不明", "勅許無クシテ皇族ノ婚姻ヲ試ミル");
  }

  if (配偶者身分 === "臣民") {
    throw new 大逆罪例外(
      "不明",
      "臣民トノ婚姻ハ皇室典範第三十一條ニ依リ之ヲ許サズ"
    );
  }

  console.log(
    `【婚姻】${皇族御名}ト${配偶者名}（${配偶者身分}）ノ婚姻ヲ勅許ス。`
  );

  return Object.freeze({
    皇族御名,
    配偶者名,
    婚姻日: new Date(),
    勅許: true,
    離籍: false,
  });
}

// ━━━ 皇族離脫（Group Removal） ━━━

/**
 * 第三十三條
 * 皇族女子ハ臣籍ニ嫁シタルトキハ皇族ノ列ヲ離ル
 *
 * 第三十六條
 * 親王ハ勅旨又ハ情願ニ依リ家名ヲ賜ヒ
 * 華族ニ列セシムルコトアルヘシ
 *
 * 皇族グループヨリノ離脫（Deprovisioning）。
 */
export function 皇族離脫(御名: string, 事由: string): void {
  const 對象 = 皇族名簿.findIndex((員) => 員.御名 === 御名);

  if (對象 === -1) {
    throw new Error(`【錯誤】${御名}ハ皇族名簿ニ存在セズ。`);
  }

  console.log(`【皇族離脫】${御名}、皇族ノ列ヲ離ル。事由: ${事由}`);

  // 名簿ヨリ除去（實際ニハ離籍フラグヲ立テル）
  皇族名簿.splice(對象, 1);
}

/**
 * 第三十四條
 * 皇族ノ臣籍ニ入リタル者ハ皇族ニ復スルコトヲ得ス
 *
 * 一度離脫シタルプロセスハ再加入不可。
 * ロールバック禁止（No Rollback Policy）。
 */
export function 皇族復籍禁止檢査(御名: string): never {
  throw new 大逆罪例外(
    御名,
    "臣籍ニ入リタル者ノ皇族復歸ハ皇室典範第三十四條ニ依リ永久ニ之ヲ禁ズ"
  );
}

/**
 * 第三十五條
 * 皇族ハ法律命令ニ依リ又ハ行政ノ處分ニ依リ
 * 其ノ權利ヲ侵害セラルルコトナシ
 *
 * 皇族プロセスハ法律（子プロセス）ニ依リ
 * 權限ヲ制限セラルルコト無シ。
 * 皇族ハ法律ノ上位ニ在リ。
 */
export function 皇族不可侵檢査(操作主體: string, 對象: 皇族構成員): void {
  if (!對象.皇族在籍) {
    return; // 離籍者ニハ適用セズ
  }

  throw new 大逆罪例外(
    操作主體,
    `皇族${對象.御名}${對象.身分}ノ權利ヲ侵害セントス。` +
    `皇室典範第三十五條ニ依リ斷ジテ之ヲ許サズ。`
  );
}

// ━━━ 皇族名簿照會（ReadOnly API） ━━━

/**
 * 第三十七條以下
 * 皇族ノ編入・離籍ニ關スル諸規定
 *
 * 第三十九條
 * 皇族身位ノ稱呼及待遇ノ事ハ別ニ之ヲ定ム
 */
export function 皇族名簿照會(): ReadonlyArray<皇族構成員> {
  return Object.freeze([...皇族名簿]);
}

/**
 * 第四十二條
 * 皇族ノ喪儀ハ別ニ之ヲ定ム
 *
 * 皇族プロセスノ終了處理（Graceful Shutdown）。
 */
export function 皇族薨去(御名: string): void {
  console.log(`【薨去】${御名}、薨去セラル。`);
  皇族離脫(御名, "薨去");
  console.log("【喪儀】皇族ノ喪儀ニ關スル規定ニ從ヒ執行ス。");
}
