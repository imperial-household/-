// ━━━ 型定義 ━━━

/** 皇族の性別 */
type 性別 = "男" | "女";

/** 皇位継承資格 */
interface 皇族 {
  readonly 御名: string;
  readonly 称号: string;
  readonly 性別: 性別;
  readonly 継承順位: number;
  readonly 即位可能: boolean;
}

/**
 * 天皇の在位状態
 *
 * ※ 宮内庁注: 旧典範では「在位」か「崩御」のみでしたが、
 *   天皇の退位等に関する皇室典範特例法（平成29年法律第63号）により
 *   「退位」が追加されました。令和元年4月30日に初めて適用。
 */
type 天皇状態 = "在位" | "崩御" | "退位";

interface 天皇プロセス {
  readonly 御名: string;
  readonly 元号: string;
  readonly 即位日: Date;
  状態: 天皇状態;
}

/**
 * 呼出元の権限（令和改定）
 *
 * ※ 現行憲法下では:
 *   - 天皇は象徴であり、国政に関する権能を有しない
 *   - 主権は国民に存する
 *   - 国会は国権の最高機関である
 *   - 皇室典範は国会が制定する法律である
 */
type 権限階級 = "天皇" | "皇嗣" | "国会" | "国民" | "外国人";

