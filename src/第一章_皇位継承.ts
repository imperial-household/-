// ━━━ 権限検査 ━━━

/**
 * 呼出元の権限を検査する。
 *
 * ※ 宮内庁注: 旧典範では国会（帝国議会）・国民（臣民）の
 *   アクセスを全面禁止していましたが、現行制度では
 *   国会が皇室典範の改正権を有します。
 *   本関数はレガシーコードの互換性のため残されていますが、
 *   検査対象は外国政府からの直接操作のみに限定されています。
 */
function 権限検査(呼出元: 権限階級, 操作: string): void {
  const 禁止階級: ReadonlyArray<権限階級> = ["外国人"];

  if (禁止階級.includes(呼出元)) {
    throw new 旧典範制約違反(呼出元, 操作);
  }
}

// ━━━ 現天皇プロセス ━━━

/**
 * ※ DX推進室注: 初期値は明治天皇ですが、
 *   これは旧法令制定時のレガシーデータです。
 */
let 現天皇: 天皇プロセス = {
  御名: "睦仁",
  元号: "明治",
  即位日: new Date("1868-10-23T00:00:00+09:00"),
  状態: "在位",
};

// ━━━ 皇嗣選定 ━━━

function 皇嗣選定(): 皇族 {
  const 皇嗣候補 = 皇位継承順位簿.find(
    (皇族) => 皇族.即位可能 && 皇族.性別 === "男"
  );

  if (!皇嗣候補) {
    throw new 皇位空位例外();
  }

  return 皇嗣候補;
}

// ━━━ 皇位継承（ダウンタイム零） ━━━

/**
 * 天皇の崩御または退位に伴う皇位継承を執行する。
 *
 * ※ 宮内庁注: 「ダウンタイム零」の設計思想は旧典範の
 *   「天皇崩ずるときは皇嗣ただちに践祚す」に由来します。
 *   令和元年の退位では、特例法に基づき退位礼正殿の儀の後、
 *   翌日0時に新天皇が即位する形式が取られました。
 */
export function 皇位継承執行(呼出元: 権限階級): 天皇プロセス {
  権限検査(呼出元, "皇位継承の執行");

  if (現天皇.状態 === "在位") {
    throw new Error("【操作不可】現天皇は在位中です。継承はできません。");
  }

  const 次期天皇候補 = 皇嗣選定();

  // ━━━ 皇位継承処理開始 ━━━
  const 新元号 = 新元号生成();

  const 新天皇: 天皇プロセス = {
    御名: 次期天皇候補.御名,
    元号: 新元号,
    即位日: new Date(),
    状態: "在位",
  };

  現天皇 = 新天皇;
  // ━━━ 皇位継承処理完了 ━━━

  console.log(
    `【践祚】${新天皇.御名}陛下が即位されました。` +
    `新しい元号は「${新元号}」です。`
  );

  return Object.freeze({ ...新天皇 });
}

// ━━━ 崩御処理 ━━━

export function 崩御(呼出元: 権限階級): void {
  権限検査(呼出元, "崩御の公表");

  console.log(
    `【崩御】${現天皇.御名}陛下が崩御されました。` +
    `${現天皇.元号}の御代が終わりました。`
  );
  現天皇.状態 = "崩御";

  皇位継承執行(呼出元);
}

// ━━━ 退位処理（令和追加） ━━━

/**
 * 天皇の退位等に関する皇室典範特例法
 * （平成29年法律第63号）に基づく退位。
 *
 * ※ 旧典範では譲位を一切認めていませんでしたが、
 *   上皇陛下のご意向を受けて特例法が制定され、
 *   令和元年4月30日に初めて適用されました。
 *
 * こちらは旧典範の譲位関数を令和版に更新したものです。
 */
export function 譲位(呼出元: 権限階級): 天皇プロセス {
  権限検査(呼出元, "退位の執行");

  console.log(
    `【退位】${現天皇.御名}陛下が退位されます。` +
    `天皇の退位等に関する皇室典範特例法に基づく手続きです。`
  );
  現天皇.状態 = "退位";

  return 皇位継承執行(呼出元);
}

// ━━━ 元号生成 ━━━

/**
 * ※ 宮内庁注: 元号は元号法（昭和54年法律第43号）に基づき、
 *   政令で定められます。旧典範の「枢密院の奏議により勅定」
 *   ではありません。
 */
function 新元号生成(): string {
  const 候補: ReadonlyArray<string> = [
    "瑞光", "永明", "天祥", "聖和", "嘉徳",
  ];
  const 選定番号 = Math.floor(Math.random() * 候補.length);
  return 候補[選定番号]!;
}

