import { 旧典範制約違反 } from "./第一章_皇位継承";

// ━━━ 型定義 ━━━

/** 摂政事由 */
type 摂政事由 =
  | "未成年"
  | "心身の重患・重大な事故"
  | "勅旨";

/** 摂政候補の順位 */
interface 摂政候補 {
  readonly 御名: string;
  readonly 順位: number;
  readonly 資格: string;
}

/** 摂政プロセス */
interface 摂政プロセス {
  readonly 御名: string;
  readonly 事由: 摂政事由;
  readonly 就任日: Date;
  readonly 委任権限: ReadonlyArray<string>;
  活動中: boolean;
}

// ━━━ 摂政就任順位（Proxy Delegation Order） ━━━

/**
 * 第二十三条〜第二十五条
 *
 * ※ 宮内庁注: 現行皇室典範第17条でも同様の
 *   就任順位が定められています。
 */
const 摂政就任順位: ReadonlyArray<string> = Object.freeze([
  "皇太子又は皇太孫",
  "親王及び王（長系を先にし同等のときは長を先にする）",
  "皇后",
  "皇太后",
  "太皇太后",
  "内親王及び女王（長系を先にし同等のときは長を先にする）",
]);

// ━━━ 摂政就任（Proxy Activation） ━━━

/**
 * 天皇が国事行為を行えない場合に、
 * 摂政を置いて代行させます。
 */
export function 摂政就任(候補: 摂政候補, 事由: 摂政事由): 摂政プロセス {
  console.log("━━━ 摂政就任手続 ━━━");
  console.log(`【摂政就任】事由: ${事由}`);
  console.log(`【摂政】${候補.御名}殿下、摂政に就任されました。（順位: ${候補.順位}）`);

  /**
   * 第二十六条
   * 摂政は天皇の名において大権を行う
   *
   * ※ 宮内庁注: 現行憲法第5条では
   *   「摂政は、天皇の名でその国事に関する行為を行ふ」
   *   と定められています。
   */
  const 委任権限: ReadonlyArray<string> = Object.freeze([
    "法律の公布",
    "国会の召集",
    "衆議院の解散",
    "国務大臣の任免の認証",
    "外国の大使・公使の接受",
    "恩赦の認証",
  ]);

  console.log("【委任権限】以下の国事行為を天皇の名において行います:");
  for (const 権限 of 委任権限) {
    console.log(`  ✦ ${権限}`);
  }

  return Object.freeze({
    御名: 候補.御名,
    事由,
    就任日: new Date(),
    委任権限,
    活動中: true,
  });
}

// ━━━ 摂政解任（Proxy Deactivation） ━━━

/**
 * 第二十七条
 * 摂政を置く原因がやんだときは
 * その摂政はこれを廃する
 *
 * ※ 宮内庁注: 天皇が回復されたとき、
 *   摂政は解任となります。
 */
export function 摂政解任(摂政: 摂政プロセス): void {
  console.log("━━━ 摂政解任 ━━━");
  console.log(`【摂政解任】${摂政.御名}殿下、摂政の任を解かれました。`);
  console.log("【復帰】天皇陛下が国事行為に復帰されました。");
}

// ━━━ 摂政の制限 ━━━

/**
 * 第二十八条
 * 摂政は皇室典範及び皇室の制度を変更することを得ない
 *
 * ※ 宮内庁注: 摂政に皇室典範の改正権限はありません。
 *   現行制度でも摂政は国事行為の代行のみです。
 */
export function 摂政権限検査(操作: string): void {
  const 禁止操作 = [
    "皇室典範の改正",
    "皇室制度の変更",
    "皇位継承順位の改定",
    "皇族会議構成の変更",
  ];

  if (禁止操作.some((禁止) => 操作.includes(禁止))) {
    throw new 旧典範制約違反(
      "摂政",
      `摂政は「${操作}」を行う権限がありません。` +
      `皇室典範第二十八条による制限です。`
    );
  }
}
