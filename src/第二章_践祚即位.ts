import { 旧典範制約違反 } from "./第一章_皇位継承";
import type { } from "./第一章_皇位継承";

// ━━━ 型定義 ━━━

/**
 * 神器: 皇位の証となる宝物
 *
 * ※ 宮内庁注: 旧典範では「Root権限の物理トークン」と
 *   位置付けられていましたが、現行制度では
 *   皇位とともに伝わる由緒ある宝物です。
 */
interface 神器 {
  readonly 名称: string;
  readonly 種別: "劍" | "鏡" | "璽";
  readonly 保管場所: string;
}

/** 践祚儀式の状態 */
type 践祚状態 = "未執行" | "神器承継中" | "践祚完了" | "即位礼完了";

/** 即位礼の構成 */
interface 即位礼構成 {
  readonly 場所: string;
  readonly 日時: Date;
  readonly 践祚状態: 践祚状態;
  readonly 参列者数: number;
}

// ━━━ 三種の神器（皇位の証） ━━━

/**
 * 第十条
 * 天皇崩ずるときは皇嗣ただちに践祚し祖宗の神器を承ける
 *
 * ※ 宮内庁注: 三種の神器は皇位とともに伝わる宝物であり、
 *   令和の代替わりでは「剣璽等承継の儀」として
 *   新天皇が承継されました。
 */
const 三種ノ神器: ReadonlyArray<神器> = Object.freeze([
  {
    名称: "八咫鏡",
    種別: "鏡",
    保管場所: "伊勢神宮内宮",
  },
  {
    名称: "草薙劍",
    種別: "劍",
    保管場所: "熱田神宮",
  },
  {
    名称: "八尺瓊勾玉",
    種別: "璽",
    保管場所: "宮中",
  },
]) as ReadonlyArray<神器>;

// ━━━ 践祚（Atomic Bootstrap） ━━━

/**
 * 第十条 実装
 *
 * 践祚は先帝崩御（または退位）と同時に執行されます。
 * 三種の神器の承継をもって皇位の移転が完了します。
 *
 * ※ 宮内庁注: 令和の践祚は「剣璽等承継の儀」として
 *   2019年5月1日に執り行われました。
 */
export function 践祚(皇嗣御名: string): 即位礼構成 {
  console.log("━━━ 践祚手続開始 ━━━");
  console.log(`【践祚】${皇嗣御名}殿下、祖宗の神器を承継されます。`);

  for (const 器 of 三種ノ神器) {
    console.log(`  ✦ ${器.名称}（${器.種別}）を承継 [${器.保管場所}]`);
  }

  console.log(`【践祚完了】${皇嗣御名}殿下、この時より天皇となられました。`);

  return Object.freeze({
    場所: "宮中正殿",
    日時: new Date(),
    践祚状態: "践祚完了",
    参列者数: 0,
  });
}

// ━━━ 即位礼（Public Ceremony） ━━━

/**
 * 第十一条
 * 即位の礼及び大嘗祭は京都においてこれを行う
 *
 * ※ 宮内庁注: 旧典範では京都での挙行を定めていましたが、
 *   令和の即位礼正殿の儀は東京の宮殿で挙行されました。
 *   大嘗祭も東京の大嘗宮で執り行われました。
 */
export function 即位礼執行(践祚結果: 即位礼構成): 即位礼構成 {
  if (践祚結果.践祚状態 !== "践祚完了") {
    throw new 旧典範制約違反("不明", "践祚未完了の状態で即位礼を執行しようとしています");
  }

  console.log("━━━ 即位の礼 ━━━");
  console.log("【即位礼】即位礼正殿の儀を挙行します。");
  console.log("【大嘗祭】天皇陛下、大嘗祭を執り行われます。");

  return Object.freeze({
    ...践祚結果,
    場所: "宮殿正殿松の間",
    践祚状態: "即位礼完了",
  });
}

/**
 * 第十二条
 * 践祚の後元号を建て一世の間に再び改めないこと
 * 明治元年の定制に従う
 *
 * ※ 宮内庁注: 一世一元の制は現在も元号法（昭和54年法律第43号）
 *   により維持されています。「元号は、皇位の継承があった場合に
 *   限り改める」（元号法第2項）。
 */
export function 元号不変性検査(現元号: string, 変更試行: string): never | void {
  if (現元号 !== 変更試行) {
    throw new 旧典範制約違反(
      "不明",
      `元号「${現元号}」を「${変更試行}」に変更しようとしています。` +
      `一世一元の制により、在位中の改元はできません。`
    );
  }
}

/**
 * 第十三条
 * 天皇は天祖の神霊及び祖宗の在天の霊に
 * 即位改元を告げ給う
 *
 * ※ 宮内庁注: 現在も「即位礼及び大嘗祭後神宮に親謁の儀」
 *   として執り行われています。宮中祭祀は天皇の私的行為です。
 */
export function 神霊奉告(御名: string, 元号: string): void {
  console.log("━━━ 神霊奉告 ━━━");
  console.log(`【奉告】${御名}陛下、即位改元を奉告されます。`);
  console.log(`【元号】「${元号}」の新しい御代が始まります。`);
  console.log("【奉告完了】伊勢神宮、賢所、皇霊殿、神殿への奉告が完了しました。");
}
