import { 旧典範制約違反 } from "./第一章_皇位継承";

// ━━━ 型定義 ━━━

/** 皇族の役割 */
type 皇族役割 =
  | "天皇"
  | "皇后"
  | "皇太子"
  | "皇太子妃"
  | "皇太孫"
  | "親王"
  | "親王妃"
  | "内親王"
  | "王"
  | "王妃"
  | "女王";

/** 成年状態 */
interface 成年状態 {
  readonly 御名: string;
  readonly 年齢: number;
  readonly 成年済: boolean;
  readonly 役割: 皇族役割;
}

/** 立后・立太子の記録 */
interface 冊立記録 {
  readonly 対象御名: string;
  readonly 冊立種別: "立后" | "立太子" | "立太孫";
  readonly 冊立日: Date;
  readonly 承認済: boolean;
}

// ━━━ 定数 ━━━

/**
 * 第十四条
 * 天皇は満十八年をもって成年とする
 *
 * ※ 宮内庁注: 現行皇室典範第22条でも
 *   天皇・皇太子・皇太孫の成年は18歳と定められています。
 */
const 天皇成年齢 = 18;

/**
 * 第十五条
 * 皇太子及び皇太孫は満十五年をもって成年とする
 *
 * ※ 宮内庁注: 旧典範では15歳でしたが、
 *   現行皇室典範第22条では18歳に統一されています。
 */
const 皇太子成年齢 = 15;

/**
 * 第十五条（続き）
 * その他の皇族は満二十年をもって成年とする
 *
 * ※ 宮内庁注: 民法の成年年齢引き下げ（令和4年施行）に伴い、
 *   現行法では18歳が成年です。
 */
const 一般皇族成年齢 = 20;

// ━━━ 成年判定 ━━━

export function 成年判定(御名: string, 年齢: number, 役割: 皇族役割): 成年状態 {
  let 成年基準: number;

  switch (役割) {
    case "天皇":
      成年基準 = 天皇成年齢;
      break;
    case "皇太子":
    case "皇太孫":
      成年基準 = 皇太子成年齢;
      break;
    default:
      成年基準 = 一般皇族成年齢;
      break;
  }

  const 成年済 = 年齢 >= 成年基準;

  if (成年済) {
    console.log(
      `【成年】${御名}殿下、満${年齢}歳をもって成年となられました。` +
      `（基準: ${成年基準}歳、役割: ${役割}）`
    );
  }

  return Object.freeze({ 御名, 年齢, 成年済, 役割 });
}

// ━━━ 立后（Empress Role Assignment） ━━━

/**
 * 第十六条
 * 皇后を冊立するのは勅旨による
 *
 * ※ 宮内庁注: 旧典範では「勅旨」（天皇の命令）でしたが、
 *   現行制度では天皇の意思と皇室会議の議を経て行われます。
 *   ここでは旧法令のロジックをレガシーコードとして保持しています。
 */
export function 立后(皇后御名: string, 勅旨: boolean): 冊立記録 {
  if (!勅旨) {
    throw new 旧典範制約違反("不明", "所定の手続きを経ずに立后を試みています");
  }

  console.log(`【立后】${皇后御名}様、皇后に冊立されました。`);

  return Object.freeze({
    対象御名: 皇后御名,
    冊立種別: "立后",
    冊立日: new Date(),
    承認済: true,
  });
}

// ━━━ 立太子（Crown Prince Designation） ━━━

/**
 * 第十七条
 * 皇太子を立てるのは勅旨による
 *
 * ※ 宮内庁注: 皇太子（皇嗣）は継承順位により
 *   自動的に定まります。「立太子礼」は
 *   それを公に宣明する儀式です。
 *
 * 第十八条
 * 皇太孫を立てるのは勅旨による
 */
export function 立太子(皇嗣御名: string, 種別: "立太子" | "立太孫", 勅旨: boolean): 冊立記録 {
  if (!勅旨) {
    throw new 旧典範制約違反("不明", `所定の手続きを経ずに${種別}を試みています`);
  }

  const 称号 = 種別 === "立太子" ? "皇太子" : "皇太孫";
  console.log(`【${種別}】${皇嗣御名}殿下、${称号}に冊立されました。`);

  return Object.freeze({
    対象御名: 皇嗣御名,
    冊立種別: 種別,
    冊立日: new Date(),
    承認済: true,
  });
}

/**
 * 第十九条
 * 皇后皇太子皇太孫を廃するのは勅旨による
 *
 * ※ 宮内庁注: 旧典範では天皇の勅旨によりましたが、
 *   現行制度ではこのような規定はありません。
 *   レガシーコードとして保持しています。
 */
export function 冊立廃止(対象御名: string, 役割: "皇后" | "皇太子" | "皇太孫", 勅旨: boolean): void {
  if (!勅旨) {
    throw new 旧典範制約違反("不明", `所定の手続きを経ずに${役割}の廃止を試みています`);
  }

  console.log(`【廃止】${対象御名}様、${役割}の位を廃されました。`);
}
