// ━━━ 型定義 ━━━

type 身位 = "皇后" | "太皇太后" | "皇太后" | "親王" | "親王妃" | "内親王" | "王" | "王妃" | "女王";

interface 皇族メンバー {
  readonly 御名: string;
  readonly 身位: 身位;
  readonly 世数: number;
  readonly 性別: "男" | "女";
  在籍: boolean;
}

// ━━━ 第5条 ━━━
// 皇后、太皇太后、皇太后、親王、親王妃、内親王、王、王妃及び女王を皇族とする。

const 皇族名簿: 皇族メンバー[] = [];

export function 皇族登録(メンバー: 皇族メンバー): void {
  皇族名簿.push(メンバー);
  console.log(`【皇族登録】${メンバー.御名}（${メンバー.身位}）を皇族名簿に登録。`);
}

export function 皇族一覧取得(): ReadonlyArray<皇族メンバー> {
  return 皇族名簿.filter((m) => m.在籍);
}

// ━━━ 第6条 ━━━
// 嫡出の皇子及び嫡男系嫡出の皇孫は、男を親王、女を内親王とし、
// 三世以下の嫡男系嫡出の子孫は、男を王、女を女王とする。

export function 身位判定(性別: "男" | "女", 世数: number): 身位 {
  if (世数 <= 2) {
    return 性別 === "男" ? "親王" : "内親王";
  }
  return 性別 === "男" ? "王" : "女王";
}

// ━━━ 第7条 ━━━
// 王が皇位を継承したときは、その兄弟姉妹たる王及び女王は、
// 特にこれを親王及び内親王とする。

export function 即位時身位昇格(即位者の兄弟姉妹: 皇族メンバー[]): void {
  for (const m of 即位者の兄弟姉妹) {
    if (m.身位 === "王" || m.身位 === "女王") {
      console.log(`【身位昇格】${m.御名}: ${m.身位} → ${m.性別 === "男" ? "親王" : "内親王"}`);
    }
  }
}

// ━━━ 第8条 ━━━
// 皇嗣たる皇子を皇太子という。皇太子のないときは、皇嗣たる皇孫を皇太孫という。

export function 皇嗣称号判定(関係: "皇子" | "皇孫"): "皇太子" | "皇太孫" {
  return 関係 === "皇子" ? "皇太子" : "皇太孫";
}

// ━━━ 第9条（旧典範に明文なし。Breaking Change） ━━━
// 天皇及び皇族は、養子をすることができない。

export function 養子検査(): never {
  throw new Error("【禁止】天皇及び皇族は養子をすることができません（第9条）。");
}

// ━━━ 第10条 ━━━
// 立后及び皇族男子の婚姻は、皇室会議の議を経ることを要する。

export function 婚姻許可(皇室会議議決: boolean): void {
  if (!皇室会議議決) {
    throw new Error("【手続不備】皇室会議の議を経ていません（第10条）。");
  }
  console.log("【婚姻許可】皇室会議の議を経て婚姻が許可されました。");
}

// ━━━ 第11条（Breaking Change: 自発的離脱を新設） ━━━
// ① 年齢十五年以上の内親王、王及び女王は、その意思に基き、
//    皇室会議の議により、皇族の身分を離れる。
// ② 親王（皇太子及び皇太孫を除く。）、内親王、王及び女王は、
//    やむを得ない特別の事由があるときは、皇室会議の議により、
//    皇族の身分を離れる。

export function 自発的離脱(
  メンバー: 皇族メンバー,
  年齢: number,
  皇室会議議決: boolean
): void {
  if (!皇室会議議決) {
    throw new Error("【手続不備】皇室会議の議を経ていません（第11条）。");
  }
  if (年齢 < 15) {
    throw new Error("【要件不足】15歳未満の皇族は自発的離脱できません（第11条）。");
  }
  メンバー.在籍 = false;
  console.log(`【皇籍離脱】${メンバー.御名}が自らの意思により皇族の身分を離れました。`);
}

// ━━━ 第12条 ━━━
// 皇族女子は、天皇及び皇族以外の者と婚姻したときは、皇族の身分を離れる。

export function 婚姻による離脱(メンバー: 皇族メンバー): void {
  if (メンバー.性別 !== "女") {
    throw new Error("【適用外】第12条は皇族女子にのみ適用されます。");
  }
  メンバー.在籍 = false;
  console.log(`【皇籍離脱】${メンバー.御名}が婚姻により皇族の身分を離れました。`);
}

// ━━━ 第13条 ━━━
// 皇族の身分を離れる親王又は王の妃並びに直系卑属及びその妃は、
// 他の皇族の意見を聴いた上で、皇族の身分を離れる。

export function 連鎖離脱(離脱者の直系卑属: 皇族メンバー[]): void {
  for (const m of 離脱者の直系卑属) {
    m.在籍 = false;
    console.log(`【連鎖離脱】${m.御名}が第13条により皇族の身分を離れました。`);
  }
}

// ━━━ 第14条 ━━━
// ① 皇族以外の女子で親王妃又は王妃となった者が、その夫を失ったときは、
//    その意思により、皇族の身分を離れることができる。
// ② やむを得ない特別の事由があるときは、皇室会議の議により離れる。
// ③ 離婚したときは、皇族の身分を離れる。

export function 妃の離脱(メンバー: 皇族メンバー, 理由: "死別" | "離婚" | "特別事由"): void {
  メンバー.在籍 = false;
  console.log(`【妃の離脱】${メンバー.御名}: ${理由}により皇族の身分を離れました。`);
}

// ━━━ 第15条 ━━━
// 皇族以外の者及びその子孫は、女子が皇后となる場合及び
// 皇族男子と婚姻する場合を除いては、皇族となることがない。

export function 皇族編入検査(理由: "皇后冊立" | "皇族男子との婚姻" | "その他"): boolean {
  if (理由 === "その他") {
    console.log("【拒否】皇族編入は第15条の例外に該当しません。");
    return false;
  }
  return true;
}
