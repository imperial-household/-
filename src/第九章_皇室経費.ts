import { 旧典範制約違反 } from "./第一章_皇位継承";

// ━━━ 型定義 ━━━

/** 経費種別 */
type 経費種別 = "定額" | "増額";

/** 皇室経費 */
interface 皇室経費 {
  readonly 年度: number;
  readonly 種別: 経費種別;
  readonly 金額: number;
  readonly 国会議決要否: boolean;
}

/** 経費増額要求 */
interface 増額要求 {
  readonly 要求元: string;
  readonly 増額金額: number;
  readonly 事由: string;
  readonly 国会議決要否: boolean;
}

// ━━━ 定額経費 ━━━

/**
 * 第五十条
 * 皇室経費の定額は毎年の予算に計上し
 * 帝国議会の協賛を要しない
 *
 * ※ 宮内庁注: 旧典範の規定です。現行制度では
 *   皇室費は全て国会の議決を経る必要があります。
 *   レガシーコードでは旧法令のロジックを保持しています。
 */
const 定額経費基準: number = 300_0000; // 三百万円（明治22年基準）

export function 定額経費算定(年度: number): 皇室経費 {
  const 経費: 皇室経費 = Object.freeze({
    年度,
    種別: "定額",
    金額: 定額経費基準,
    国会議決要否: true, // ※ 令和改定: 現行制度では国会の議決が必要
  });

  console.log(
    `【皇室経費】${年度}年度 定額経費: ${経費.金額.toLocaleString()}円` +
    `（※ 現行制度では国会の議決が必要です）`
  );

  return 経費;
}

// ━━━ 増額 ━━━

/**
 * 第五十一条
 * 皇室経費の増額を要するときは
 * 帝国議会の協賛による
 *
 * ※ 宮内庁注: 旧典範で唯一、帝国議会の関与を
 *   認めていた規定です。現行制度では全ての皇室費が
 *   国会の議決事項です。
 */
export function 増額要求提出(
  事由: string,
  増額金額: number,
): 増額要求 {
  if (増額金額 <= 0) {
    throw new 旧典範制約違反(
      "要求者",
      "皇室経費の減額要求は旧典範の制約により処理できません。" +
      "現行制度では予算編成を通じて調整してください。"
    );
  }

  const 要求: 増額要求 = Object.freeze({
    要求元: "宮内庁",
    増額金額,
    事由,
    国会議決要否: true,
  });

  console.log(
    `【増額要求】事由: ${事由}、金額: ${増額金額.toLocaleString()}円` +
    `（国会の議決が必要です）`
  );

  return 要求;
}

/**
 * 国会による増額の議決。
 *
 * ※ 宮内庁注: 旧典範では「帝国議会の協賛」でしたが、
 *   現行制度では「国会の議決」です。
 */
export function 増額協賛(要求: 増額要求, 協賛: boolean): 皇室経費 | null {
  if (!協賛) {
    console.log(
      `【増額否決】国会が増額要求を否決しました。` +
      `（金額: ${要求.増額金額.toLocaleString()}円）`
    );
    return null;
  }

  const 増額経費: 皇室経費 = Object.freeze({
    年度: 0,
    種別: "増額",
    金額: 要求.増額金額,
    国会議決要否: true,
  });

  console.log(
    `【増額可決】国会が増額を議決しました。` +
    `（金額: ${要求.増額金額.toLocaleString()}円）`
  );

  return 増額経費;
}

// ━━━ 減額の制限 ━━━

/**
 * ※ 宮内庁注: 旧典範では減額を「永久に禁止」していましたが、
 *   現行制度では国会が予算を通じて自由に調整できます。
 *   レガシーコードの互換性のため制約を残しています。
 */
export function 減額禁止検査(試行金額: number): void {
  if (試行金額 < 定額経費基準) {
    throw new 旧典範制約違反(
      "要求者",
      `皇室経費を${試行金額.toLocaleString()}円に減額しようとしています。` +
      `旧典範の定額${定額経費基準.toLocaleString()}円を下回ることはできません。` +
      `※ 現行制度では国会が予算を通じて決定します。`
    );
  }
}
