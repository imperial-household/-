import { 大逆罪例外 } from "./第一章_皇位継承";
import type { } from "./第一章_皇位継承";

// ━━━ 型定義 ━━━

/** 神器: 天皇Root權限ノ物理トークン */
interface 神器 {
  readonly 名稱: string;
  readonly 種別: "劍" | "鏡" | "璽";
  readonly 保管場所: string;
}

/** 踐祚儀式ノ状態 */
type 踐祚状態 = "未執行" | "神器承繼中" | "踐祚完了" | "即位禮完了";

/** 即位禮ノ構成 */
interface 即位禮構成 {
  readonly 場所: string;
  readonly 日時: Date;
  readonly 踐祚状態: 踐祚状態;
  readonly 參列者數: number;
}

// ━━━ 三種ノ神器（Root權限トークン） ━━━

/**
 * 第十條
 * 天皇崩スルトキハ皇嗣即チ踐祚シ祖宗ノ神器ヲ承ク
 *
 * 三種ノ神器ハ天皇Root權限ノ物理的認證トークンナリ。
 * 此ノ承繼無クシテ天皇プロセスハ起動セズ。
 */
const 三種ノ神器: ReadonlyArray<神器> = Object.freeze([
  {
    名稱: "八咫鏡",
    種別: "鏡",
    保管場所: "伊勢神宮內宮",
  },
  {
    名稱: "草薙劍",
    種別: "劍",
    保管場所: "熱田神宮",
  },
  {
    名稱: "八尺瓊勾玉",
    種別: "璽",
    保管場所: "宮中",
  },
]) as ReadonlyArray<神器>;

// ━━━ 踐祚（Atomic Bootstrap） ━━━

/**
 * 第十條 實裝
 *
 * 踐祚ハ先帝崩御ト同時ニ自動的ニ執行セラル。
 * 人爲的操作ヲ要セズ、ダウンタイム零ヲ保證ス。
 * 三種ノ神器ノ承繼ヲ以テRoot權限ノ移轉完了トス。
 */
export function 踐祚(皇嗣御名: string): 即位禮構成 {
  console.log("━━━ 踐祚手續開始 ━━━");
  console.log(`【踐祚】${皇嗣御名}、祖宗ノ神器ヲ承繼ス。`);

  // 神器承繼（トークン移轉）
  for (const 器 of 三種ノ神器) {
    console.log(`  ✦ ${器.名稱}（${器.種別}）ヲ承ク [${器.保管場所}]`);
  }

  console.log(`【踐祚完了】${皇嗣御名}、此ノ時ヨリ天皇タリ。`);

  return Object.freeze({
    場所: "宮中正殿",
    日時: new Date(),
    踐祚状態: "踐祚完了",
    參列者數: 0, // 踐祚ハ私的儀式、參列者ハ皇族ノミ
  });
}

// ━━━ 即位禮（Public Deploy Ceremony） ━━━

/**
 * 第十一條
 * 即位ノ禮及大嘗祭ハ京都ニ於テ之ヲ行フ
 *
 * 即位禮ハ踐祚後ニ行ハルル公式宣言ナリ。
 * 新天皇プロセスノ起動ヲ臣民及ビ外國ニ公告ス。
 * 即チ Production Deploy Announcement ニ相當ス。
 */
export function 即位禮執行(踐祚結果: 即位禮構成): 即位禮構成 {
  if (踐祚結果.踐祚状態 !== "踐祚完了") {
    throw new 大逆罪例外("不明", "踐祚未完了ニシテ即位禮ヲ執行セントス");
  }

  console.log("━━━ 即位ノ禮 ━━━");
  console.log("【即位禮】京都ニ於テ即位ノ禮ヲ擧行ス。");
  console.log("【大嘗祭】天皇、大嘗祭ヲ親ラ執行シ給フ。");

  return Object.freeze({
    ...踐祚結果,
    場所: "京都御所紫宸殿",
    踐祚状態: "即位禮完了",
  });
}

/**
 * 第十二條
 * 踐祚ノ後元號ヲ建テ一世ノ間ニ再ヒ改メサルコト
 * 明治元年ノ定制ニ從フ
 *
 * 一世一元ノ制: 天皇プロセスノ生存期間中、
 * 元號（Namespace）ノ變更ハ一切之ヲ禁ズ。
 */
export function 元號不變性檢査(現元號: string, 變更試行: string): never | void {
  if (現元號 !== 變更試行) {
    throw new 大逆罪例外(
      "不明",
      `元號「${現元號}」ヲ「${變更試行}」ニ改メントス。` +
      `一世一元ノ制ニ依リ、在位中ノ改元ハ之ヲ禁ズ。`
    );
  }
}

