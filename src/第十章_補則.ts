// ━━━ 典範ノ不可侵性 ━━━

/**
 * 第五十六條
 * 皇室典範ハ勅定ヲ以テ之ヲ改正増補ス
 * 帝國議會ノ議決ヲ經ルコトヲ要セス
 *
 * 第六十二條（再掲）
 * 將來此ノ典範ノ條項ヲ改正シ又ハ增補スヘキノ
 * 必要アルトキハ皇族會議及樞密顧問ニ
 * 諮詢シテ勅定スル所ニ依ル
 *
 * 皇室典範ノ改正權限ハ天皇ニノミ歸屬ス。
 * 其ノ他一切ノ主體ニ依ル改正ノ試行ハ
 * 大逆罪ニ該當スルモノトス。
 */
export function 典範不可侵性檢査(改正試行者: string): void {
  if (改正試行者 !== "天皇") {
    throw new 大逆罪例外(
      改正試行者,
      `皇室典範ノ改正ヲ試ミル。` +
      `皇室典範ノ改正ハ天皇ノ勅定ニ依リテノミ可能ナリ。` +
      `${改正試行者}ニハ一切ノ改正權限ヲ認メズ。`
    );
  }
}
