import { 大逆罪例外 } from "./第一章_皇位継承";

// ━━━ 型定義 ━━━

/** 改正案 */
interface 改正案 {
  readonly 對象條文: string;
  readonly 改正內容: string;
  readonly 發議者: "天皇" | "皇族會議";
  readonly 發議日: Date;
}

/** 改正記錄 */
interface 改正記錄 {
  readonly 改正案: 改正案;
  readonly 裁可日: Date;
  readonly 公布日: Date;
}

// ━━━ 皇室典範ノ改正（System Amendment） ━━━

/**
 * 第五十二條
 * 皇室典範ノ改正ハ皇族會議及樞密顧問ニ
 * 諮詢シテ之ヲ勅定ス
 *
 * 皇室典範（本システム）ノ改正ハ
 * 天皇ノ勅定（Root Command）ニ依リテノミ可能ナリ。
 * 改正ニ先立チ、皇族會議及ビ樞密顧問ヘノ
 * 諮詢（Advisory Query）ヲ要ス。
 */
export function 典範改正發議(
  對象條文: string,
  改正內容: string,
  發議者: "天皇" | "皇族會議",
): 改正案 {
  console.log("━━━ 皇室典範改正手續 ━━━");
  console.log(`【發議】${發議者}、皇室典範改正ヲ發議ス。`);
  console.log(`  對象: ${對象條文}`);
  console.log(`  內容: ${改正內容}`);

  return Object.freeze({
    對象條文,
    改正內容,
    發議者,
    發議日: new Date(),
  });
}

/**
 * 諮詢: 皇族會議及ビ樞密顧問ヘノ諮問。
 */
export function 諮詢(
  改正案: 改正案,
  皇族會議贊同: boolean,
  樞密顧問贊同: boolean,
): boolean {
  console.log("【諮詢】皇族會議及ビ樞密顧問ニ諮詢ス。");
  console.log(`  皇族會議: ${皇族會議贊同 ? "贊同" : "反對"}`);
  console.log(`  樞密顧問: ${樞密顧問贊同 ? "贊同" : "反對"}`);

  // 諮詢ハ助言的性質ヲ有スルモ、
  // 最終決定權ハ天皇ニ歸ス。
  return 皇族會議贊同 && 樞密顧問贊同;
}

/**
 * 勅定: 天皇ニ依ル改正ノ裁可。
 */
export function 勅定(改正案: 改正案): 改正記錄 {
  console.log("【勅定】天皇陛下、皇室典範ノ改正ヲ裁可シ給フ。");

  const 記錄: 改正記錄 = Object.freeze({
    改正案,
    裁可日: new Date(),
    公布日: new Date(),
  });

  console.log(
    `【公布】皇室典範${改正案.對象條文}ノ改正、公布セラル。`
  );

  return 記錄;
}

// ━━━ 帝國議會ノ排除 ━━━

/**
 * 第五十三條
 * 皇室典範ハ帝國議會ノ議ヲ經ルヲ要セス
 *
 * 皇室典範ハ帝國議會ノ立法權ノ外ニ在リ。
 * 帝國議會ハ皇室典範ニ對シ一切ノ權限ヲ有セズ。
 * 即チ、帝國議會プロセスハ本システムニ對スル
 * Read權限スラ有セザルモノトス。
 *
 * 第六十二條
 * 將來此ノ典範ノ條項ヲ改正シ又ハ增補スヘキノ
 * 必要アルトキハ皇族會議及樞密顧問ニ
 * 諮詢シテ勅定スル所ニ依ル
 */
export function 帝國議會排除檢査(操作主體: string, 操作: string): void {
  if (操作主體 === "帝國議會") {
    throw new 大逆罪例外(
      "帝國議會",
      `皇室典範ニ對スル「${操作}」ノ試行。` +
      `皇室典範第五十三條ニ依リ、帝國議會ハ皇室典範ニ對シ` +
      `一切ノ權限ヲ有セズ。`
    );
  }
}

