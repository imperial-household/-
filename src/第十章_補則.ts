import { 旧典範制約違反 } from "./第一章_皇位継承";

// ━━━ 型定義 ━━━

/** 改正案 */
interface 改正案 {
  readonly 対象条文: string;
  readonly 改正内容: string;
  readonly 発議者: string;
  readonly 発議日: Date;
}

/** 改正記録 */
interface 改正記録 {
  readonly 改正案: 改正案;
  readonly 議決日: Date;
  readonly 公布日: Date;
}

// ━━━ 皇室典範の改正 ━━━

/**
 * 第五十二条
 * 皇室典範の改正は皇族会議及び枢密顧問に
 * 諮詢してこれを勅定する
 *
 * ※ 宮内庁注: 旧典範では天皇の勅定によりましたが、
 *   現行皇室典範は国会の立法手続きで改正されます。
 *   本関数はレガシーシステムの改正提案を記録するものです。
 */
export function 典範改正発議(
  対象条文: string,
  改正内容: string,
  発議者: string,
): 改正案 {
  console.log("━━━ 皇室典範改正手続 ━━━");
  console.log(`【発議】${発議者}が皇室典範改正を発議しました。`);
  console.log(`  対象: ${対象条文}`);
  console.log(`  内容: ${改正内容}`);

  return Object.freeze({
    対象条文,
    改正内容,
    発議者,
    発議日: new Date(),
  });
}

/**
 * 諮詢: 関係機関への諮問。
 *
 * ※ 宮内庁注: 旧典範では皇族会議と枢密顧問への
 *   諮詢でしたが、現行制度では法制審議会や
 *   皇室会議への諮問に相当します。
 */
export function 諮詢(
  改正案: 改正案,
  皇族会議賛同: boolean,
  枢密顧問賛同: boolean,
): boolean {
  console.log("【諮詢】関係機関に諮問しました。");
  console.log(`  皇族会議: ${皇族会議賛同 ? "賛同" : "反対"}`);
  console.log(`  枢密顧問: ${枢密顧問賛同 ? "賛同" : "反対"}`);

  return 皇族会議賛同 && 枢密顧問賛同;
}

/**
 * 改正の議決。
 *
 * ※ 宮内庁注: 旧典範では「勅定」（天皇の決定）でしたが、
 *   現行制度では国会の議決により改正されます。
 *   関数名を「改正議決」に変更しました。
 */
export function 改正議決(改正案: 改正案): 改正記録 {
  console.log("【議決】皇室典範の改正が議決されました。");

  const 記録: 改正記録 = Object.freeze({
    改正案,
    議決日: new Date(),
    公布日: new Date(),
  });

  console.log(
    `【公布】皇室典範${改正案.対象条文}の改正が公布されました。`
  );

  return 記録;
}

// ━━━ 国会と皇室典範の関係（令和改定） ━━━

/**
 * 第五十三条
 * 皇室典範は帝国議会の議を経ることを要しない
 *
 * ※ 宮内庁注: 旧典範の最も重要な規定の一つでしたが、
 *   現行制度では完全に逆転しています。
 *   皇室典範は国会が制定する法律であり、
 *   改正には国会の議決が必須です（憲法第2条・第41条）。
 *
 *   本関数は旧典範の「帝国議会排除」を
 *   「国会権限確認」に転換しました。
 */
export function 国会権限確認(操作主体: string, 操作: string): void {
  if (操作主体 === "国会") {
    console.log(
      `【権限確認】国会は「${操作}」の権限を有しています。` +
      `皇室典範は国会が制定する法律です（憲法第2条）。`
    );
  }
}

// ━━━ 皇室典範と憲法の関係 ━━━

/**
 * 第五十四条〜第五十五条
 *
 * ※ 宮内庁注: 旧典範は帝国憲法と並立する最高法規でしたが、
 *   現行皇室典範は日本国憲法の下位にある法律です。
 *   憲法に反する規定は効力を有しません。
 */
export function 憲法矛盾検査(典範条文: string, 憲法条文: string): void {
  console.log("【矛盾検査】皇室典範と日本国憲法の整合性を検査します。");
  console.log(`  典範: ${典範条文}`);
  console.log(`  憲法: ${憲法条文}`);
  console.log("【検査完了】矛盾は検出されませんでした。");
}

// ━━━ 典範の改正権限 ━━━

/**
 * 第五十六条・第六十二条
 *
 * ※ 宮内庁注: 旧典範では天皇のみが改正権限を持ちましたが、
 *   現行制度では国会が唯一の立法機関です（憲法第41条）。
 *   天皇は国政に関する権能を有しません（憲法第4条）。
 *
 *   本関数は旧典範の「天皇以外の改正禁止」を
 *   「国会による改正手続きの確認」に転換しました。
 */
export function 典範改正権限検査(改正試行者: string): void {
  if (改正試行者 === "国会") {
    console.log(
      "【権限確認】国会は皇室典範を改正する権限を有しています。"
    );
    return;
  }

  if (改正試行者 === "天皇") {
    console.log(
      "【注意】現行制度では天皇は皇室典範の改正権限を持ちません。" +
      "天皇は日本国の象徴であり、国政に関する権能を有しません。" +
      "改正は国会の立法手続きで行ってください。"
    );
    return;
  }

  throw new 旧典範制約違反(
    改正試行者,
    `皇室典範の改正は国会の立法手続きにより行われます。` +
    `${改正試行者}には直接の改正権限はありません。` +
    `国会議員を通じて法案を提出してください。`
  );
}
