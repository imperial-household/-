// ━━━ 型定義 ━━━

type 議員カテゴリ =
  | "皇族"
  | "衆議院議長"
  | "衆議院副議長"
  | "参議院議長"
  | "参議院副議長"
  | "内閣総理大臣"
  | "宮内庁長官"
  | "最高裁判所長官"
  | "最高裁判所裁判官";

interface 議員 {
  readonly 氏名: string;
  readonly カテゴリ: 議員カテゴリ;
  readonly 予備: boolean;
}

interface 皇室会議 {
  readonly 議長: 議員;
  readonly 議員一覧: ReadonlyArray<議員>;
  readonly 予備議員一覧: ReadonlyArray<議員>;
}

// ━━━ 第28条 ━━━
// 皇室会議は、議員十人でこれを組織する。

const 定数 = 10;

// ━━━ 第29条（Breaking Change: 議員構成を完全刷新） ━━━
// 議員は、皇族二人、衆議院及び参議院の議長及び副議長、
// 内閣総理大臣、宮内庁の長並びに最高裁判所の長たる裁判官
// 及びその他の裁判官一人を以て、これに充てる。

const 議員枠: ReadonlyArray<{ カテゴリ: 議員カテゴリ; 定員: number }> = Object.freeze([
  { カテゴリ: "皇族", 定員: 2 },
  { カテゴリ: "衆議院議長", 定員: 1 },
  { カテゴリ: "衆議院副議長", 定員: 1 },
  { カテゴリ: "参議院議長", 定員: 1 },
  { カテゴリ: "参議院副議長", 定員: 1 },
  { カテゴリ: "内閣総理大臣", 定員: 1 },
  { カテゴリ: "宮内庁長官", 定員: 1 },
  { カテゴリ: "最高裁判所長官", 定員: 1 },
  { カテゴリ: "最高裁判所裁判官", 定員: 1 },
]);

export function 議員構成検証(議員一覧: ReadonlyArray<議員>): boolean {
  const 実議員 = 議員一覧.filter((m) => !m.予備);
  if (実議員.length !== 定数) {
    console.log(`【構成異常】議員数が${定数}人ではありません: ${実議員.length}人`);
    return false;
  }
  for (const 枠 of 議員枠) {
    const 該当数 = 実議員.filter((m) => m.カテゴリ === 枠.カテゴリ).length;
    if (該当数 !== 枠.定員) {
      console.log(`【構成異常】${枠.カテゴリ}: 定員${枠.定員}に対し${該当数}人`);
      return false;
    }
  }
  return true;
}

// ━━━ 第30条 ━━━
// 議員となる皇族及び最高裁判所の裁判官は、各々互選する。

export function 互選(
  カテゴリ: "皇族" | "最高裁判所裁判官",
  候補者一覧: ReadonlyArray<string>
): string {
  if (候補者一覧.length === 0) {
    throw new Error(`【互選不成立】${カテゴリ}の候補者がいません（第30条）。`);
  }
  console.log(`【互選】${カテゴリ}の互選を実施。候補${候補者一覧.length}名。`);
  return 候補者一覧[0]; // 実際は互選プロトコルによる
}

// ━━━ 第31条 ━━━
// 議員がその資格を失ったときは、補欠の議員を任命する。

export function 補欠任命(
  失格議員: 議員,
  予備議員一覧: ReadonlyArray<議員>
): 議員 | null {
  const 補欠 = 予備議員一覧.find(
    (m) => m.カテゴリ === 失格議員.カテゴリ && m.予備
  );
  if (!補欠) {
    console.log(`【補欠不在】${失格議員.カテゴリ}の予備議員がいません（第31条）。`);
    return null;
  }
  console.log(`【補欠任命】${失格議員.氏名} → ${補欠.氏名}（${補欠.カテゴリ}）`);
  return 補欠;
}

// ━━━ 第32条 ━━━
// 皇室会議に、予備議員十人を置く。

export function 予備議員検証(予備一覧: ReadonlyArray<議員>): boolean {
  const 予備数 = 予備一覧.filter((m) => m.予備).length;
  if (予備数 !== 定数) {
    console.log(`【予備議員不足】予備議員数: ${予備数}/${定数}`);
    return false;
  }
  return true;
}

// ━━━ 第33条（Breaking Change: 議長 = 総理大臣） ━━━
// ① 皇室会議の議長は、内閣総理大臣が、これにあたる。
// ② 皇室会議は、議長が、これを招集する。
// ③ 第3条、第16条第2項、第18条及び第20条の場合には、
//    四人以上の議員の要求があるときは、これを招集しなければならない。

export function 会議招集(
  議長: 議員,
  招集理由: "通常" | "緊急",
  要求議員数?: number
): void {
  if (議長.カテゴリ !== "内閣総理大臣") {
    throw new Error("【権限不足】議長は内閣総理大臣でなければなりません（第33条）。");
  }
  if (招集理由 === "緊急" && (要求議員数 ?? 0) < 4) {
    throw new Error("【要件不足】緊急招集には4人以上の議員の要求が必要です（第33条③）。");
  }
  console.log(`【会議招集】${議長.氏名}が皇室会議を招集しました（${招集理由}）。`);
}

// ━━━ 第34条 ━━━
// 皇室会議の議事は、第3条、第16条第2項、第20条及び第36条の場合には、
// 出席した議員の三分の二以上の多数でこれを決し、
// その他の場合には、過半数でこれを決する。

type 議決種別 = "特別多数" | "過半数";

export function 議決(
  種別: 議決種別,
  出席議員数: number,
  賛成数: number
): boolean {
  const 閾値 = 種別 === "特別多数"
    ? Math.ceil((出席議員数 * 2) / 3)
    : Math.ceil(出席議員数 / 2) + (出席議員数 % 2 === 0 ? 1 : 0);
  const 成立 = 賛成数 >= 閾値;
  console.log(
    `【議決】${種別}: 出席${出席議員数}名中 賛成${賛成数}名（閾値${閾値}）→ ${成立 ? "可決" : "否決"}`
  );
  return 成立;
}

// ━━━ 第35条 ━━━
// 皇室会議の議事は、これを公表しない。
// 但し、この法律及び他の法律に基づき議に付せられた事項の結果については、
// この限りでない。

export function 議事録公開判定(法律に基づく議題: boolean): boolean {
  if (!法律に基づく議題) {
    console.log("【非公開】第35条により議事は公表しません。");
    return false;
  }
  console.log("【公開】法律に基づく議題の結果は公表します（第35条但書）。");
  return true;
}

// ━━━ 第36条 ━━━
// 議員は、自分の利害に特別の関係のある議事には、参与することができない。

export function 利害関係排除(議員名: string, 関係あり: boolean): boolean {
  if (関係あり) {
    console.log(`【利害排除】${議員名}は利害関係があるため本議事に参与できません（第36条）。`);
    return false;
  }
  return true;
}

